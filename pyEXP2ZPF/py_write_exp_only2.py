def Write_tcm_file(file_tcm, elem2, maxtemp):
    with open(file_tcm, 'w') as f:
        f.write('\n')
        f.write('\ngo data ')
        f.write('\nsw user ESPEI_opt_params.tdb ')
        f.write('\n')
        f.write('\ndef-ele ')
        f.write(elem2[0] + ' ' + elem2[1])
        f.write('\n')
        f.write('\nget')
        f.write('\n')
        f.write('\ngo p-3')
        f.write('\ns-c n=1 p=1e5 ')
        f.write('\ns-c t=')
        f.write(str(maxtemp))
        f.write('\ns-c x(')
        f.write(elem2[1]+')=0.3')
        f.write('\n')
        f.write('\nc-e')
        f.write('\nc-e')
        f.write('\n')
        f.write('\ns-a-v 1 x(')
        f.write(elem2[1] + ') ,,,,')
        f.write('\ns-a-v 2 t 300 ')
        f.write(str(maxtemp) + ' ,,,,')
        f.write('\n')
        f.write('\nadd ,,,,')
        f.write('\nmap')
        f.write('\n')
        f.write('\npo')
        f.write('\ns-l f')
        f.write('\n')
        f.write('\napp y myExptTLINV_sorted.exp')
        f.write('\n0')
        f.write('\n1')
        f.write('\n')
        f.write('\npl ,,,,')
        f.write('\n')
        f.write('\nset-inter')
        f.write('\n')
    f.close()

